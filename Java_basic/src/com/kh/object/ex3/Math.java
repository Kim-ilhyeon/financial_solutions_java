package com.kh.object.ex3;

public class Math {
	// 필드
	
	
	// 생성자
	
	// 메소드
	int adder (int n1) {
		return n1 + n1;
	}

	int adder (int n1, int n2) {
		return n1 + n2;
	}
	
	double adder (double d1, double d2) {
		return d1 + d2;
	}
	
	String adder (int n1, String str) {
		return n1 + str;
	}

	String adder (String str, int n1) {
		return n1 + str;
	}
	
}
